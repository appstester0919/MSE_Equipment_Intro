// Helper function to get equipment by ID (optional, but can be useful)
function getEquipmentById(floorId, roomId, equipmentId) {
    const floor = appData.floors.find(f => f.id === floorId);
    if (!floor) return null;
    const room = floor.rooms.find(r => r.id === roomId);
    if (!room) return null;
    return room.equipment.find(e => e.id === equipmentId);
}

